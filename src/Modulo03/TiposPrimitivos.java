/* TIPOS PRIMITIVOS */
/* No Java temos vários tipos primitivos que armazenam numéros, isso acontece porque antigamente as máquinas tinham
* quase nada de memória, dessa forma o Java tinha que fazer uma boa otimização dos recursos */
/* Um outro motivo é que Java é multiplataforma, o código pode rodar em máquinas que tem pouquissima memória,
* então nesse caso a otimização é muito importante */

// NUMEROS INTEIROS
// byte - Número em até 1 byte (-128 até 127)
// short - Número de 2 bytes (-32.768 - 32.767)
// int - Número de 4 bytes (-2.147.483.648 - 2.147.483.647
// long - Número em escala bem grande (acima)

// NUMEROS COM PARTE FRANCIONARIAS
// float - 4 bytes (6 - 7 digitos) - Float pode perder um pouco do valor a depender do numero
// double - 8 bytes (15 digitos) - Temos precisao do valor

// ESCRITA
// char - para um caracter apenas
// boolean - para True ou False

// String não é um tipo primitivo em Java. String é um objeto em Java, ou seja, faz parte do pacote java.lang






