// ACCESS MODIFIERS
/* Em java temos o Access Modifiers, eles nos ajudam a proteger nossos atributos e metódos, isso faz com que
* nosso codigo seja seguro, pois um outro métodoo de uma outro objeto não tenha acesso a um determinado métodoo
* a menos que se tenha dado permissão */

// 1 - Default (sem nada na frente) - Somente clases do mesmo pacote (pasta), poderá acessar esse métodoo ou atributo
// 2 - Private - Acessivel somente dentro da classe que foi declarado (somente os métodos da classe podem acessar esse métodoo)
// 3 - Protected - Acessível dentro do mesmo pacote (pasta) ou também acessível de uma Subclasses em pasta diferente.
// 4 - Public - Accessível de qualquer lugar no código.



// STATIC KEYWORD
// Esse palavra diz se o Atributo ou o Métodoo pertende a Classe ou se pertence ao objeto
// Se tem o STATIC significa que posso acessar aquele valor sem instanciar a classe
// Se não tem o STATIC significa que esse valor pertence ao objeto, ou seja, só posso usar ao instanciar a classe;
// Usamos STATIC quando todos os objetos que foram instanciados precisam usar o mesmo valor que foi definido, ou quando queremos definir constantes usando o final.



